function divider(input)
{
    const spacer = `=`.repeat(20);
    console.log(`\n` + spacer, input, spacer, `\n`);
}

divider("SWITCH-STATEMENTS")
const position = 0;
switch (position) {
    case 0:
        console.log(`Ausgeschaltet`);
        break;

    case 1:
        console.log(`Eingeschaltet`);
        break;
}